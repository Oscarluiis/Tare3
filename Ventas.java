    private void b_totalMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_b_totalMousePressed
        // TODO add your handling code here:
        int n1 = (int)v_cantidad.getValue();
        int n2 = (int)v_precio.getValue();
        int r = n1 * n2;
        int re=0;
        String nom = txt_nombre.getText();
        
        v_resultado.setValue(r);
       
       
        if (v_si.isSelected()){
            
            v_desc.setValue(r*0.05);
           
            
        }
        if (v_no.isSelected()){
            v_desc.setValue(0);
        }
        
        if(op_pago.getSelectedItem().equals("Efectivo")){
           re = 1;
        }
        else if(op_pago.getSelectedItem().equals("Tarjeta Credito")){
            re = 5;
        }
        if (op_pago.getSelectedItem().equals("Tarjeta Debito")){
            re = 8;
        }
        
        v_pago.setValue(re);
        
       lbl_re.setText("Cliente: "+nom+ " Su total es: "+ (re+r) );
    }//GEN-LAST:event_b_totalMousePressed

